package main

import "fmt"

func main() {
	var name string

	var name_with_value = "Tasya Dea"

	name = "Tasya Dea Ardelia"
	fmt.Println(name)

	name = "Tasya"
	fmt.Println(name)

	fmt.Println(name_with_value)

	name_var := "Tasya"
	fmt.Println(name_var)

	var (
		firstName  = "Tasya"
		middleName = "Dea Ardelia"
		lastName   = "Tambunan"
	)

	fmt.Println(firstName)
	fmt.Println(middleName)
	fmt.Println(lastName)

}
